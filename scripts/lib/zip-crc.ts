/**
 * CRC-32 (IEEE 802.3, polynomial 0xEDB88320) — the ZIP entry checksum.
 *
 * Shared by the reference reader (zip-reader.ts, normative logic) and the fixture
 * writer (zip-writer.ts, test infrastructure) so neither depends on the other for
 * this one unambiguous standard primitive.
 */

const CRC_TABLE: Uint32Array = (() => {
  const table = new Uint32Array(256);
  for (let n = 0; n < 256; n++) {
    let c = n;
    for (let k = 0; k < 8; k++) {
      c = c & 1 ? 0xedb88320 ^ (c >>> 1) : c >>> 1;
    }
    table[n] = c >>> 0;
  }
  return table;
})();

export function crc32(bytes: Uint8Array): number {
  let crc = 0xffffffff;
  for (let i = 0; i < bytes.length; i++) {
    crc = CRC_TABLE[(crc ^ bytes[i]) & 0xff] ^ (crc >>> 8);
  }
  return (crc ^ 0xffffffff) >>> 0;
}
